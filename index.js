function sum(a, b) {
  return a + b;
}

function subtract(a, b) {
  return a - b;
}

function multiply(a, b) {
  return a * b;
}

// eslint-disable-next-line no-console
console.log('Sum: ', sum(2, 3));
// eslint-disable-next-line no-console
console.log('Subtract: ', subtract(5, 2));
// eslint-disable-next-line no-console
console.log('Multiply: ', multiply(4, 2));
